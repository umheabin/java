package day02;

public class RalationalOperatorEx {
	public static void main(String[] args) {
		int num1 = 1 , num2 = 2;
		
		System.out.println(num1 + " > " + num2  + " : " + (num1 > num2));
		System.out.println(num1 + " >= " + num2  + " : " + (num1 >= num2));
		System.out.println(num1 + " < " + num2  + " : " + (num1 < num2));
		System.out.println(num1 + " <= " + num2  + " : " + (num1 <= num2));
		System.out.println(num1 + " == " + num2  + " : " + (num1 == num2));
		System.out.println(num1 + " ! " + num2  + " : " + (num1 != num2));
		
	}
	

}
