package day01;

public class VariableEx01 {

	public static void main(String[] args) {
		/*변수 선언 방법*
		1.변수 선언만 함.
		*변수타입 변수명;*
		2.변수를 선언하고 초기화함
		*변수타입 변수명 = 값*/
		//문자형 변수 ch1를 선언//
		char ch1;
		//문자형 변수 ch2를 선언하고 문자 A로 초기화
		char ch2 = 'A';
		//변수는 초기화를 하고 사용해야 합니다//
		System.out.println(ch2);
		ch1 = '+';
		System.out.println(ch1);

	}

}
